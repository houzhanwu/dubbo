package cn.cttic.pojo.dynamic;

public class VehiclePositionPojo {
	
	//VehicleNo
	private String vehicleNo;
	// PlateColor
	private int plateColor;
	
	private String startPositionTime;
	
	private String endPositionTime;

	public String getVehicleNo() {
		return vehicleNo;
	}

	public void setVehicleNo(String vehicleNo) {
		this.vehicleNo = vehicleNo;
	}

	public int getPlateColor() {
		return plateColor;
	}

	public void setPlateColor(int plateColor) {
		this.plateColor = plateColor;
	}

	public String getStartPositionTime() {
		return startPositionTime;
	}

	public void setStartPositionTime(String startPositionTime) {
		this.startPositionTime = startPositionTime;
	}

	public String getEndPositionTime() {
		return endPositionTime;
	}

	public void setEndPositionTime(String endPositionTime) {
		this.endPositionTime = endPositionTime;
	}
	
	
	
}
